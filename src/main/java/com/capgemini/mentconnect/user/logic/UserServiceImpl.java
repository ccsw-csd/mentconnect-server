package com.capgemini.mentconnect.user.logic;

import com.capgemini.mentconnect.common.exception.EntityNotFoundException;
import com.capgemini.mentconnect.user.dto.UserSearchDto;
import com.capgemini.mentconnect.user.model.UserEntity;
import com.capgemini.mentconnect.user.model.UserRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.PageRequest;
import org.springframework.data.domain.Sort;
import org.springframework.stereotype.Service;

import java.util.List;
import java.util.Optional;

/**
 * @author amirzoya
 *
 *         Clase que implementa los m√©todos de la interfaz UserService
 *
 */
@Service
public class UserServiceImpl implements UserService {

    @Autowired
    UserRepository userRepository;

    @Override
    public Optional<UserEntity> autenticate(String username, String password) {

        return this.userRepository.findByUsernameAndPassword(username, password);
    }

    @Override
    public UserEntity get(Long id) throws EntityNotFoundException {

        return userRepository.findById(id).orElseThrow(EntityNotFoundException::new);
    }

    @Override
    public List<UserEntity> findAll() {

        return userRepository.findAll();
    }

    @Override
    public Page<UserEntity> findPage(UserSearchDto dto) {

        return userRepository.findAll(dto.getPageable());
    }
}
